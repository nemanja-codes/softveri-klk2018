    private void jButtonObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiActionPerformed
        int selektovaniRed = jTableaAlat.getSelectedRow();
        if(selektovaniRed == -1) {
            JOptionPane.showMessageDialog(this, "Nijedan alar nije izabran!", "Greska", JOptionPane.ERROR_MESSAGE);
        } else {
            ModelTabeleAlat mta = (ModelTabeleAlat) jTableaAlat.getModel();
            mta.obrisiAlat(selektovaniRed);
        }
    }//GEN-LAST:event_jButtonObrisiActionPerformed

    private void jButtonDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajActionPerformed
        Alat a = (Alat) jComboBoxAlat.getSelectedItem();
        if(a.getStanje() > 0) {
            ModelTabeleAlat mta = (ModelTabeleAlat) jTableaAlat.getModel();
            mta.dodajAlat(a);
            a.setStanje(a.getStanje() - 1);
        } else {
            JOptionPane.showMessageDialog(this, "Alata nema na stanju", "Greska", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButtonDodajActionPerformed

    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        
        try {
            Inzenjer ulogovani = Controller.getInstance().getUlogovaniInzenjer();
            String imePrezime = ulogovani.getIme()+" "+ulogovani.getPrezime();
            String naziv = jTextFieldNaziv.getText();
            String proiz = jTextFieldProizvodjac.getText();
            int radniVek = Integer.parseInt(jTextFieldRadniVek.getText());
            int godProizvodnje = Integer.parseInt(jTextFieldGodina.getText());
            if(godProizvodnje<1900 || godProizvodnje>2018) {
                JOptionPane.showMessageDialog(this, "Pogresna godina!", "Greska", JOptionPane.ERROR_MESSAGE);
            }
            TipMasine tip = (TipMasine) jComboBoxTip.getSelectedItem();
            Lokacija lokacija = (Lokacija) jComboBoxLokacija.getSelectedItem();

            int idLokacije = lokacija.getId();
            boolean postojiMasinaNaLokaciji = Controller.getInstance().postojiMasina(idLokacije);
            if(postojiMasinaNaLokaciji) {
                JOptionPane.showMessageDialog(this, "Greska masina!");
                return;
            }
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            
            Date datum = sdf.parse(jTextFieldDatum.getText());
            if(datum.before(new Date())) {
                JOptionPane.showMessageDialog(this, "Greska datum!");
                return;
            }
            
            Masina m = new Masina(-1, naziv, godProizvodnje, datum, tip, lokacija, proiz, radniVek, imePrezime);
            ModelTabeleAlat mta = (ModelTabeleAlat) jTableaAlat.getModel();
            List<Alat> alati = mta.getAlati();
            
            m.setAlati(alati);
            boolean uspesno = Controller.getInstance().ubaciMasinu(m);
            // unos u bazu
            
            
            
        } catch (ParseException ex) {
            Logger.getLogger(UnosMasineForma.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(UnosMasineForma.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_jButtonSacuvajActionPerformed

