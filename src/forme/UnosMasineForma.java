    private void popuniComboBox() {
        List<Lokacija> sveLokacije = Controller.getInstance().vratiLokacije();
        for (Lokacija lokacija : sveLokacije) {
            jComboBoxLokacija.addItem(lokacija);
        }
        
        List<Alat> sviAlati = Controller.getInstance().vratiAlate();
        for (Alat alat : sviAlati) {
            jComboBoxAlat.addItem(alat);
        }
    }
